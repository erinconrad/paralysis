%{
This is the code to perform the analysis for the Persyst paralysis project.
It currently requires that you have the data in a separate data folder. 


To run the analysis, first open up paralysis_stats.m and adjust the
parameters to specify the correct data and results paths.

Then go to the folder containing paralysis_stats and type
>> paralysis_stats

This will run the analysis.

%}